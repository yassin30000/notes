// ENCAPSULATION

    // definition
        // the idea of hiding states from users
        // bringing together data and the methods which operate on that data into a single unit

    // notes

        // common use for encapsulation is keeping states private from users
        // we could do this by having private objects and public objects

        // public objects
            // accessable by other objects

        // private objects
            // only accessible by the object that it is in
            // closures are private objects, or 'encapsulated data'
